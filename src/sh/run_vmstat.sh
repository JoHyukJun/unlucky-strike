# vmstat 명령어 설명:
# vmstat은 Unix/Linux 시스템의 가상 메모리, 프로세스, CPU, I/O 등의 통계를 실시간으로 표시하는 명령어입니다.
# 주요 용도: 시스템 성능 모니터링, 메모리 사용량, CPU 사용률, 디스크 I/O 등을 확인.
# 옵션:
# - 첫 번째 인수 (2): 샘플링 간격(초 단위).
# - 두 번째 인수 (5): 반복 횟수. 여기서는 5번 실행 후 종료.
# - 출력 예시: 프로세스 수, 메모리(메모리, 버퍼, 캐시), 스왑, I/O, 시스템, CPU 통계.
# 주의: root 권한이 필요할 수 있으며, 시스템 부하를 고려해 사용하세요.
vmstat 1 > /JLOG/vmstat.log &